package buyui.cit260.quest.model;

import java.io.Serializable;
import java.util.Objects;

/**
 *
 * @author Arkadiusz
 */
public class ResourceActionScene implements Serializable{
    //class instance variables
    private String description;
    private int quantity;
    private int value;
    private double travelTime;

    public ResourceActionScene() {
    }
    

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public double getTravelTime() {
        return travelTime;
    }

    public void setTravelTime(int travelTime) {
        this.travelTime = travelTime;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 67 * hash + Objects.hashCode(this.description);
        hash = 67 * hash + this.quantity;
        hash = 67 * hash + this.value;
        hash = 67 * hash + (int) (Double.doubleToLongBits(this.travelTime) ^ (Double.doubleToLongBits(this.travelTime) >>> 32));
        return hash;
    }

    @Override
    public String toString() {
        return "ResourceActionScene{" + "description=" + description + ", quantity=" + quantity + ", value=" + value + ", travelTime=" + travelTime + '}';
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final ResourceActionScene other = (ResourceActionScene) obj;
        if (!Objects.equals(this.description, other.description)) {
            return false;
        }
        if (this.quantity != other.quantity) {
            return false;
        }
        if (this.value != other.value) {
            return false;
        }
        if (Double.doubleToLongBits(this.travelTime) != Double.doubleToLongBits(other.travelTime)) {
            return false;
        }
        return true;
    }

   
    
    
}
